#include "ft_printf.h"

void	ft_str(char *arg, int *len)
{
	int	i;

	i = 0;
	if (arg == NULL)
	{
		write(1, "(null)", 6);
		(*len) += 6;
		return ;
	}
	while (arg[i] != '\0')
	{
		ft_putchar_len(arg[i], len);
		i++;
	}
}
